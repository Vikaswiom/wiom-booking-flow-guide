SELECT
  CASE WHEN TIMESTAMP < '2026-03-26' THEN 'PRE' ELSE 'POST' END AS period,
  EVENT_NAME,
  COUNT(DISTINCT USER_ID) AS distinct_users
FROM PROD_DB.PUBLIC.CLEVERTAP_CUSTOMER
WHERE EVENT_NAME ILIKE '%serviceab%'
  AND ((TIMESTAMP >= '2026-02-20' AND TIMESTAMP < '2026-03-26')
       OR (TIMESTAMP >= '2026-03-28' AND TIMESTAMP < '2026-04-13'))
GROUP BY 1, 2
ORDER BY 1, distinct_users DESC
